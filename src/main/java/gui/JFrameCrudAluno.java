    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");// Formato pr√© definido para as datas
        Date aux = null;
        try {
            aux = sdf.parse(data.getText());
        } catch (ParseException ex) {
            Logger.getLogger(JFrameCrudAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Alunos aluno = new Alunos();
        aluno.setNome(nome.getText());
        aluno.setCora(Float.parseFloat(cora.getText()));
        aluno.setCpf(Long.parseLong(cpf.getText()));
        aluno.setDataNasc(aux);
        aluno.setRa(ra.getText());
        aluno.setTelefone(telefone.getText());
        AlunoDao aDao = new AlunoDao();
        
        aDao.gravar(aluno);
        
        this.dispose();
        JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso!","SUCESSO", JOptionPane.INFORMATION_MESSAGE);
       
        JFrameAluno tela = new JFrameAluno();
        tela.setLocationRelativeTo(this);
        tela.setVisible(true);
        
       
    }//GEN-LAST:event_botaoCadastrarActionPerformed

