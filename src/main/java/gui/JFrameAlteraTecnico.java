    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String cpfConsultado = cpf.getText();
        
        TecnicoAdmDao tecnicoAdm = new TecnicoAdmDao();
        t = tecnicoAdm.consultarUm(cpfConsultado);
        nome.setText(t.getNome());
        cpf1.setText(Long.toString(t.getCpf()));
        data.setText(sdf.format(t.getDataNasc()));
        telefone.setText(t.getTelefone());
        codFun.setText(Long.toString(t.getCodFuncional()));
        salario.setText(Double.toString(t.getSalario()));
        departamento.setText(t.getDepartamento());
    }//GEN-LAST:event_consultarActionPerformed

