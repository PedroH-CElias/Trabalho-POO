    private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");// Formato pré definido para as datas
        Date aux = null;
        try {
            aux = sdf.parse(data.getText());
        } catch (ParseException ex) {
            Logger.getLogger(JFrameCrudProfessor.class.getName()).log(Level.SEVERE, null, ex);
        }
        FuncionarioTecnicoAdministrativo tecnicoAdm = new FuncionarioTecnicoAdministrativo();
        tecnicoAdm.setNome(nome.getText());
        tecnicoAdm.setCpf(Long.parseLong(cpf.getText()));
        tecnicoAdm.setDataNasc(aux);
        tecnicoAdm.setTelefone(telefone.getText());
        tecnicoAdm.setCodFuncional(Integer.parseInt(codFun.getText()));
        tecnicoAdm.setSalario(Float.parseFloat(salario.getText()));
        tecnicoAdm.setDepartamento(departamento.getText());
        TecnicoAdmDao tDao = new TecnicoAdmDao();
        tDao.alterar(tecnicoAdm, cpf.getText());

        this.dispose();
        JOptionPane.showMessageDialog(null, "Técnico Administrativo cadastrado com sucesso!","SUCESSO", JOptionPane.INFORMATION_MESSAGE);

        JFrameTecnico tela = new JFrameTecnico();
        tela.setLocationRelativeTo(this);
        tela.setVisible(true);
    }//GEN-LAST:event_botaoAlterarActionPerformed

